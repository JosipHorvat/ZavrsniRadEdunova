    private void lstPodaciValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPodaciValueChanged
      if(evt.getValueIsAdjusting()){
          return;
      }
      entitet=lstPodaci.getSelectedValue();
      if(entitet== null){
          return;
      }
      // potencijalno bolje rješenje je korištenje Reflection API
        //https://docs.oracle.com/javase/tutorial/reflect/index.html
        
        txtNaziv.setText(entitet.getNaziv());
        txtModel.setText(entitet.getModel());
        txtPrijedjeniKm.setText(entitet.getUkupnoPredjenihKm().toString());
        txtRegistracija.setText(entitet.getRegistracijskaOznaka());
        chbOsigurano.setSelected(entitet.getOsigurano());
        
        dpDatumProizvodnje.setDate(entitet.getDatumProizvodnje().toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDate());
        
        
        
        
        
    }//GEN-LAST:event_lstPodaciValueChanged

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
       
        lblPoruka.setText("");
        entitet = new Vozilo();
        
        postaviVrijednostiUEntitet();
        
      try {
            obrada.create();
            ucitajPodatke();
            ocistiPolja();
        } catch (MyException ex) {
           lblPoruka.setText(ex.getPoruka());
        }           
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnIzmijeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmijeniActionPerformed
      entitet= lstPodaci.getSelectedValue();
      if(entitet==null){
          return;
      }
      postaviVrijednostiUEntitet();
      
      try{
          obrada.update();
          ucitajPodatke();
          ocistiPolja();
      }catch(MyException e){
          lblPoruka.setText(e.getPoruka());
      }
        
    }//GEN-LAST:event_btnIzmijeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        entitet= lstPodaci.getSelectedValue();
          if(entitet==null){
            return;
         }
          obrada.setEntitet(entitet);
        
        try{
          obrada.delete();
          ucitajPodatke();
          ocistiPolja();
      }catch(MyException e){
          lblPoruka.setText(e.getPoruka());
      }
       
    }//GEN-LAST:event_btnObrisiActionPerformed

